    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (txtIdioma.getText().equals(""))
    {
        JOptionPane.showMessageDialog(null, "É necessário preencher o nome do Idioma ! ","ATENÇÃO",JOptionPane.INFORMATION_MESSAGE);
        txtIdioma.setBackground(java.awt.Color.RED);
        txtIdioma.requestFocus();
    }
    else
    {
        if (opcao == 1) 
        {
            Idioma cat = new Idioma();

            cat.setI_ID(Integer.parseInt(txtID.getText()));
            cat.setI_Nome(txtIdioma.getText().toUpperCase());
                    
            if (this.idiomaDAO.inserir(cat) == true)
            {
                String id = txtID.getText();                               
                String nome = txtIdioma.getText();
                JOptionPane.showMessageDialog(null, "Idioma '" + txtIdioma.getText() + "' adicionado com sucesso!");
                
            } 
            else 
            {
                JOptionPane.showMessageDialog(null, "Erro ao adicionar Idioma!");
            }
        } 
        else if (opcao == 2) 
        {
            Idioma idioma = new Idioma();
            idioma.setI_ID(Integer.parseInt(txtID.getText()));
            idioma.setI_Nome(txtIdioma.getText().toUpperCase());

            if (this.idiomaDAO.editar(idioma) == true) 
            {
                JOptionPane.showMessageDialog(null, "Idioma modificado com sucesso!");
            } 
            else 
            {
                JOptionPane.showMessageDialog(null, "Erro ao modificar Idioma!");
            }
        }
        preencheTabela();
        Limpar();
        Desabilitar();
        
        btnInserir.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
        btnCancelar.setEnabled(false);
        btnSalvar.setEnabled(false);
        
        rbID.setEnabled(true);
        rbIdioma.setEnabled(true);
    }
    }//GEN-LAST:event_btnSalvarActionPerformed


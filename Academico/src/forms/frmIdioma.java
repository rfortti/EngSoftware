    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        Idioma idioma = new Idioma();
        idioma.setI_ID(Integer.parseInt(txtID.getText()));

        int dialogButton = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir o Idioma selecionado ?", "Warning", 0);// 0 = botão Sim e Não

        if (dialogButton == JOptionPane.YES_OPTION) {
            if (this.idiomaDAO.excluir(idioma) == true) {
                JOptionPane.showMessageDialog(null, "Idioma '" + txtIdioma.getText() + "' excluido com sucesso!");
                Limpar();
                Desabilitar();
                btnInserir.setEnabled(true);
                btnAlterar.setEnabled(false);
                btnExcluir.setEnabled(false);
                btnCancelar.setEnabled(false);
                btnSalvar.setEnabled(false);
                
                rbID.setEnabled(true);
                rbIdioma.setEnabled(true);

                preencheTabela();
            } else {
                JOptionPane.showMessageDialog(null, "Não é possível excluir o idioma, pois há alunos ou professores vinculados!");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


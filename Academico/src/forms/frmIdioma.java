    private void preencheTabela() {
        ArrayList<Idioma> idioma = new ArrayList<Idioma>();
        idioma = this.idiomaDAO.getIdiomasByID();
        
        DefaultTableModel tabela = (DefaultTableModel) tbIdioma.getModel();
        tabela.setNumRows(0);
               
        //Verifica se retornou algum valor da consulta.
        if (idioma == null)
        {
            return;
        }
        
        //a verificacao assima é por que esse modelo de for(ForEach|For Aprimorado|Enhanced For)
        //não aceita uma lista nula.
        for (Idioma i : idioma) 
        {
            if (i != null) 
            {
                Object[] obj = new Object[]{
                    i.getI_ID(),
                    i.getI_Nome()
                };
                tabela.addRow(obj);
            }
        }
    }
    
